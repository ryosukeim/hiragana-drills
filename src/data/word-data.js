// Filter words by level
export function getWordsByLevel(level, kanaSet = 'hiragana') {
    return wordData.filter(word => word.level === level);
}

// Filter words by category
export function getWordsByCategory(category) {
    return wordData.filter(word => word.category === category);
}

// Get random word from pool
export function getRandomWord(level, kanaSet = 'hiragana') {
    const words = getWordsByLevel(level, kanaSet);
    return words[Math.floor(Math.random() * words.length)];
}

// Get word by ID
export function getWordById(id) {
    return wordData.find(word => word.id === id);
}
